    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int index = lstEstudiantes.getSelectedIndex();
        if(index == -1){
        Estudiante es =LAB7.estudianteGlobal.get(index);
        
        int option= JOptionPane.showConfirmDialog(this,"Â¿Deseas elminar a"+es.getNombre(),"?",JOptionPane.WARNING_MESSAGE);
        if(option== JOptionPane.OK_OPTION){
            LAB7.estudianteGlobal.remove(es);
        }        
        actualizarLista();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

