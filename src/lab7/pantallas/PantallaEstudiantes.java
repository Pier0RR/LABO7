    void actualizarEstudiante(Estudiante estudiante){
        
        for(int i=0;i<LAB7.estudianteGlobal.size();i++){
            Estudiante es = LAB7.estudianteGlobal.get(i);
            if(es.hashCode()== estudiante.hashCode()){
                LAB7.estudianteGlobal.set(i,estudiante);
            }
        }
        actualizarLista();
    }
    
    void agregarEstudiante(Estudiante estudiante){
        LAB7.estudianteGlobal.add(estudiante);
        actualizarLista();
    }
    
    void actualizarLista(){
        modelo.clear();
        for(Estudiante es : LAB7.estudianteGlobal){
            modelo.addElement(es.getTexto());
        }
    }
    
